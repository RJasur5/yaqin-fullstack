def normalize_region_name(name: str) -> str:
    if not name:
        return ""
        
    name = name.strip()
    
    for ru_name, uz_name in RU_TO_UZ.items():
        if name.lower() == ru_name.lower():
            return uz_name.lower()
            
    return name.lower()

def get_region_variants(name: str) -> list[str]:
    if not name:
        return []
    name = name.strip().lower()
    variants = {name}
    for ru, uz in RU_TO_UZ.items():
        if name == ru.lower() or name == uz.lower():
            variants.add(ru)
            variants.add(uz)
    return list(variants)

